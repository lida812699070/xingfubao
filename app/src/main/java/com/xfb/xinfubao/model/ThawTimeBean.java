package com.xfb.xinfubao.model;

public class ThawTimeBean {

    /**
     * hours : 0
     * seconds : 40
     * month : 0
     * year : 0
     * minutes : 28
     * day : 0
     */

    private int hours;
    private int seconds;
    private int month;
    private int year;
    private int minutes;
    private int day;

    public int getHours() {
        return hours;
    }

    public void setHours(int hours) {
        this.hours = hours;
    }

    public int getSeconds() {
        return seconds;
    }

    public void setSeconds(int seconds) {
        this.seconds = seconds;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }
}